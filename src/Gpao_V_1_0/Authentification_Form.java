    private void btn_connActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_connActionPerformed

        // execution de la requete de verification de l'existance de l'utilisateur dans le syst√®me gpao_v_1
        try {

           Connection conn = DriverManager.getConnection("jdbc:derby:Gpao_v_1"); //  recuperation de la connexion

            String sql = " select * from utilisateur where login = ? and password = ?";
            prs = conn.prepareStatement(sql);
            prs.setString(1, txt_login.getText());
            prs.setString(2, txt_pwd.getText());

            rs = prs.executeQuery();
            if (rs.next()) {

                JOptionPane.showMessageDialog(null, "Nom utilisateur et Mot de passe sont correct ");
                this.dispose();
                Menu_Gpao mgp = new Menu_Gpao();
                mgp.setVisible(true);

            } else {

                JOptionPane.showMessageDialog(null, "Nom utilisateur et Mot de passe sont incorrect ");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "impossible de recuperer la connexion voir l'administrateur merci");
        }


    }//GEN-LAST:event_btn_connActionPerformed

