    private void TabMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabMouseClicked
        // Affichage des éléments du tableaux dans les champs de saisir

        txt_numero_sect.setText(String.valueOf(Tab.getValueAt(Tab.getSelectedRow(), 0)));

        txt_main_sect.setText(String.valueOf(Tab.getValueAt(Tab.getSelectedRow(), 1)));
        txt_num_sous_sec.setText(String.valueOf(Tab.getValueAt(Tab.getSelectedRow(), 2)));

        txt_Main_SS.setText(String.valueOf(Tab.getValueAt(Tab.getSelectedRow(), 3)));
        cbx_machine.setSelectedItem(Tab.getValueAt(Tab.getSelectedRow(), 4));
        txt_Machine.setText(String.valueOf(Tab.getValueAt(Tab.getSelectedRow(), 5)));
        txt_desig.setText((String) Tab.getValueAt(Tab.getSelectedRow(), 6));
        txt_to_hor.setText(String.valueOf(Tab.getValueAt(Tab.getSelectedRow(), 7)));

        txt_nbe_poste.setText(String.valueOf(Tab.getValueAt(Tab.getSelectedRow(), 8)));

        txt_capci_nom.setText(String.valueOf(Tab.getValueAt(Tab.getSelectedRow(), 9)));
        cbx_th.setSelectedItem(Tab.getValueAt(Tab.getSelectedRow(), 10));


    }//GEN-LAST:event_TabMouseClicked

