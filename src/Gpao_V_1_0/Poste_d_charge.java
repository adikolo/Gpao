    private void btn_suppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suppActionPerformed
        // Pour supprimer dans la base de donnée

        try {
            conn = DriverManager.getConnection("jdbc:derby:Gpao_v_1");

        } catch (SQLException ex) {
            Logger.getLogger(Suppression_Article.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (JOptionPane.showConfirmDialog(null, "attention vous voulez Supprimer un poste de charge, vous etes sur?",
                "Supprimer Poste de charge", JOptionPane.YES_NO_OPTION) == JOptionPane.OK_OPTION) {

            try {
                String requete = "delete from Poste_de_charge  where  numero_section=  '" + txt_numero_sect.getText() + "' and numero_sous_section= '" + txt_num_sous_sec.getText() + "' and est_machine= '" + cbx_machine.getSelectedItem() + "' ";
                prst = conn.prepareStatement(requete);
                prst.execute();
                Tab.setModel(DbUtils.resultSetToTableModel(rst));
                affichage();
                vider();
                JOptionPane.showMessageDialog(null, "Poste de charge supprimé avec succès !!!");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "impossible de supprimé le poste de charge !!!" + ex.getMessage());
            }

        }
    }//GEN-LAST:event_btn_suppActionPerformed

