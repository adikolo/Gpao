    private void btn_enrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_enrActionPerformed
        try {
            // Pour faire un enregistrement dans la base de donnée

            conn = DriverManager.getConnection("jdbc:derby:Gpao_v_1");
            String sql = "insert into Poste_de_charge (numero_section,main_d_oeuvre_section ,numero_sous_section,main_d_oeuvre_sous_section,est_machine,main_d_oeuvre_machine, designation, taux_horaire_ou_forfait,nombre_de_postes,capacite_nominale,type_taux_horaire_ou_forfait) values(?,?,?,?,?,?,?,?,?,?,?)";
            prst = conn.prepareStatement(sql);
            prst.setString(1, (String) txt_numero_sect.getText());
            prst.setString(2, txt_main_sect.getText());
            prst.setString(3, (String) txt_num_sous_sec.getText());
            prst.setString(4, txt_Main_SS.getText());
            prst.setString(5, (String) cbx_machine.getSelectedItem());
            prst.setString(6, txt_Machine.getText());
            prst.setString(7, (String) txt_desig.getText());
            prst.setString(8, (String) txt_to_hor.getText());
            prst.setString(9, (String) txt_nbe_poste.getText());
            prst.setString(10, txt_capci_nom.getText());
            prst.setString(11, (String) cbx_th.getSelectedItem());
            prst.execute();
            affichage();
            JOptionPane.showMessageDialog(null, "insertion Reussi !!! Merci");
            vider();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "veuillez remplir tout les champs" + ex.getMessage());

        }
    }//GEN-LAST:event_btn_enrActionPerformed

